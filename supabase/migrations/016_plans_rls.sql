--
-- Включает Row-Level Security на таблице `plans` (тарифные планы).
-- Это публичный справочник — все могут его читать, но никто кроме
-- service_role не может изменять данные через клиентский SDK.
--
-- До этой миграции таблица была без RLS, что приводило к
-- предупреждению Supabase Security Advisor «Table publicly accessible».
--
-- Идемпотентна: безопасно применять повторно.
-- ═══════════════════════════════════════════════════════════════════

-- Включить RLS
ALTER TABLE plans ENABLE ROW LEVEL SECURITY;

-- Разрешить чтение всем (это публичный справочник тарифов).
-- Запись/изменение/удаление НЕ разрешены — то есть автоматически
-- запрещены для всех ролей кроме service_role (он обходит RLS).
DROP POLICY IF EXISTS "plans_public_read" ON plans;
CREATE POLICY "plans_public_read" ON plans
  FOR SELECT
  USING (true);

